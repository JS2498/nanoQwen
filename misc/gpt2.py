"""1. What does model.to(device) actually do

* autocast - Converts some of the operations to float16 fastening the training and some layers ops use the float32
* torch.compile - creates optimized computation graph. Sees the entire code and tries to dynamically fuse the operations for execution on GPU. This avoids the data transfer between the GPU kernels i.e., Streaming Multiprocessors (SMs) and High Bandwidth Memory (HBM)
* Memory Hierarchy:
  * Speed to read and write: SRAM >> HBM >> DRAM
  * Memory size: SRAM << HBM << DRAM

* The memory inside SMs (SRAM) are the L1 and L2 caches
*  Read about FlashAttention in detail
    * Uses online-softmax trick
    * The block of $V {\rm softmax}(QK^T/\sqrt{d})$ is done insider the SM itself
    * Even though it uses more flops but is faster since there is no data transfer with HBM

* Since the matix multiplication is split across kernels its better to have multiples of 2^x as the matrix dims. For example here the vocab_size

### Distributed Data Parallel
"""