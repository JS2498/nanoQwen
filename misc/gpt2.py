# learning rate decay

# cosine deacy with warmup
# for inital steps the lr is incereased linearly
# then cosine decay (1+cos(pi*decay_ratio)*0.5 is applied to lr
# the decay ratio of current decay step and no. of decay steps


max_lr = 3e-4
min_lr = max_lr * 0.1
warmup_steps = 10
max_steps = 50

def get_lr(itr):
  if itr < warmup_steps:
    return max_lr * (itr+1)/ warmup_steps

  if itr > max_steps:
    return min_lr

  decay_ratio = (itr - warmup_steps) / (max_steps - warmup_steps)
  assert 0 <= decay_ratio <= 1

  coeff = 0.5 * (1.0 + math.cos(math.pi * decay_ratio)) # 1 --> 0

  return min_lr + coeff * (max_lr - min_lr)

