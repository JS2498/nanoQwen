class CausalSelfAttention(nn.Module):
  def __init__(self, config):
    super().__init__()

    assert config.n_embed % config.n_head == 0

    # this block contains all 3: Q,K,V
    self.c_attn = nn.Linear(config.n_embed, 3 * config.n_embed)

    self.c_proj = nn.Linear(config.n_embed, config.n_embed)
    # Flag for scaling the proj layer by the no. of residual layers
    # this is done to make sure the SD inside residual terms remain close to 1
    self.c_proj.NANOGPT_SCALE_INIT = 1.0

    self.n_head = config.n_head
    self.n_embed = config.n_embed

    self.register_buffer("bias", torch.tril(torch.ones(config.block_size, config.block_size)).view(1,1, config.block_size, config.block_size))

  def forward(self, x):
    B, T, C = x.size() # batch size, context length, embedding dims


    qkv = self.c_attn(x)
    q, k, v = qkv.split(self.n_embed, dim=2)
    k = k.view(B, T, self.n_head, C // self.n_head) # (B, T, nh, hs)
    q = q.view(B, T, self.n_head, C // self.n_head)
    v = v.view(B, T, self.n_head, C // self.n_head)


    # calculate attention
    q = q.transpose(1,2) # (B, nh, T, hs)
    k = k.transpose(1,2) # (B, nh, T, hs)
    v = v.transpose(1,2) # (B, nh, T, hs)

    # why is it q @ k^T
    # att = (q @ k.transpose(-2, -1)) * (1.0 / math.sqrt(k.size(-1)))
    # att = att.masked_fill(self.bias[:,:,:T,:T] == 0, float('-inf'))
    # att = F.softmax(att, dim=-1)
    # y = att @ v

    # flash attention
    y = F.scaled_dot_product_attention(q, k, v, is_causal=True)

    # concatenate
    y = y.transpose(1,2).contiguous().view(B, T, C)
    y = self.c_proj(y)

    return y

class MLP(nn.Module):
  def __init__(self, config):
    super().__init__()
    self.c_fc = nn.Linear(config.n_embed, 4 * config.n_embed)
    self.gelu = nn.GELU(approximate='tanh')
    self.c_proj = nn.Linear(4 * config.n_embed, config.n_embed)
    # Flag for scaling the proj layer by the no. of residual layers
    # this is done to make sure the SD inside residual terms remain close to 1
    self.c_proj.NANOGPT_SCALE_INIT = 1.0

  def forward(self, x):
    x = self.c_fc(x)
    x = self.gelu(x)
    x = self.c_proj(x)
    return x

class Block(nn.Module):
  def __init__(self, config):
    super().__init__()
    self.ln_1 = nn.LayerNorm(config.n_embed)
    self.attn = CausalSelfAttention(config)
    self.ln_2 = nn.LayerNorm(config.n_embed)
    self.mlp = MLP(config)

  def forward(self,x):
    x = x + self.attn(self.ln_1(x))
    x = x + self.mlp(self.ln_2(x))

    return x

class GPT(nn.Module):
  def __init__(self, config):
    super().__init__()
    self.config = config

    self.transformer = nn.ModuleDict(dict(
        wte = nn.Embedding(config.vocab_size, config.n_embed),
        wpe = nn.Embedding(config.block_size, config.n_embed),
        h = nn.ModuleList([Block(config) for _ in range(config.n_layer)]),
        ln_f = nn.LayerNorm(config.n_embed),
    ))
    self.lm_head = nn.Linear(config.n_embed, config.vocab_size, bias=False)

    # weight sharing
    self.transformer.wte.weight = self.lm_head.weight

    # weights initialization
    self.apply(self._init_weights)

  def _init_weights(self, module):
    if isinstance(module, nn.Linear):
      std = 0.02
      if hasattr(module, 'NANOGPT_SCALE_INIT'):
        std *= (2*self.config.n_layer) ** (-0.5)
      torch.nn.init.normal_(module.weight, mean=0.0, std=0.02)
      if module.bias is not None:
        torch.nn.init.zeros_(module.bias)

    elif isinstance(module, nn.Embedding):
      torch.nn.init.normal_(module.weight, mean=0.0, std=0.02)


  def forward(self, idx, targets=None):

    B, T = idx.shape
    assert T <= self.config.block_size, f"Cannot forward sequence of length {T} and block size {B}"

    pos = torch.arange(0, T, dtype=torch.long, device=idx.device)
    tok_emb = self.transformer.wte(idx) # (B,T,n_embed)
    pos_emb = self.transformer.wpe(pos) # (T,n_embed)

    x = tok_emb + pos_emb
    for block in self.transformer.h:
      x = block(x)

    x = self.transformer.ln_f(x)
    logits = self.lm_head(x) # (B,T,vocab_size)

    loss = None

    if targets is not None:
      loss = F.cross_entropy(logits.view(-1, logits.size(-1)), targets.view(-1)) # (B*T, vocab_size)

    return logits, loss


